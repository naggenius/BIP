package com.socgen.bip.hierarchy;

public class ResultHierarchy {

	int ident;
	int cpIdent;
	String name;
	int ligneCnt;
	String actInd;
	String manInd;
	int level;
	String selInd;
	
	public int getIdent() {
		return ident;
	}
	public void setIdent(int ident) {
		this.ident = ident;
	}
	public int getCpIdent() {
		return cpIdent;
	}
	public void setCpIdent(int cpIdent) {
		this.cpIdent = cpIdent;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLigneCnt() {
		return ligneCnt;
	}
	public void setLigneCnt(int ligneCnt) {
		this.ligneCnt = ligneCnt;
	}
	public String getActInd() {
		return actInd;
	}
	public void setActInd(String actInd) {
		this.actInd = actInd;
	}
	public String getManInd() {
		return manInd;
	}
	public void setManInd(String manInd) {
		this.manInd = manInd;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public String getSelInd() {
		return selInd;
	}
	public void setSelInd(String selInd) {
		this.selInd = selInd;
	}
	
	

}
